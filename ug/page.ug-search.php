	<h2>User Guide - Search Panel</h2>

		<p>The search panel is the entry point of Onyx Fixture Finder.</p>

		<p>This User Guide will help you to use it.</p>

	<h3>Overview</h3>

		<p><img src="ug/img/search-general.png" alt="Search Panel Overview" /></p>

		<ol>
			<li>Manufacturer</li>
			<li>Name</li>
			<li>Number of DMX Channel</li>
			<li>Reset DMX Chart</li>
			<li>Select Channel Type</li>
		</ol>

	<h3>1 &amp; 2 - Manufacturer and Fixture name</h3>

		<p>These fields are described in their dedicated User Guide:</p>

			<ol>
				<li><a pop href="./<?php echo _UG_ManufacturerFixture_; ?>">Display all manufacturer fixtures</a></li>
				<li><a pop href="./<?php echo _UG_SpecificFixture_; ?>">Search a fixture by its name</a></li>
			</ol>

	<h3>3 - Number of DMX Channel</h3>

		<p>The number of DMX Channel is used to search, but also to add in the form as many select channel type as number of DMX Channel set.</p>

		<p>You can set manually the value between 1 and 512, or use the + and - buttons.</p>

	<h3>4 - Reset DMX Chart</h3>

		<p>This button allow to quickly reset the select channel type to "Any".</p>

		<p><strong>Note: It does not reset the search settings, manufacturer, name and DMX Count.</strong></p>

	<h3>5 - Select Channel Type</h3>

		<p>To set a type to a channel, click it, and select through the restricted list:</p>

		<p><img src="ug/img/select-channel-type.png" alt="Select Channel Type opened" /></p>

		<p>If your look-up parameter is not available in this list, set the "Channel Type List" to "Full" in the search settings:
		<br />
		<img src="ug/img/search-settings_parameter-list-full.png" alt="Channel Type List set to Full" /></p>

		<p>If you want Search Settings on top of the main form, simply double click on the "Gear Icon". Perform the same action to get back Search Settings at its original position.</p>

		<p><strong>Note: Your navigator can be really slower with the channel type list set to full due to the high level of parameters available</strong></p>

