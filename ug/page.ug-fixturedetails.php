<div class="form userguide">
	<h2>User Guide - Fixture details</h2>

		<p>Once your search is complete, you can access to the Fixture Details to get more informations.</p>

		<p>This User Guide will describes the informations available and the Onyx Fixture Finder features.</p>

		<h3>Access to the Fixture Details</h3>

		<p>To access to the Fixture Details, you have to click on its name:
		<br /><img src="ug/img/access-fixture-details.png" alt="Link to Fixture Details" /></p>

	<h3>Fixture Details Overview</h3>

		<p><img src="ug/img/fixture-details-window.png" alt="Fixture Details Window" /></p>

		<ol>
			<li>Manufacturer</li>
			<li>Name</li>
			<li>Wheels</li>
			<li>DMX Charts with DMX Channel type &amp; number</li>
			<li>Fixture profile changelog</li>
			<li>Switch Fixture preset</li>
			<li>Close the window</li>
		</ol>

	<h3>1 &amp; 2 - Fixture name</h3>

		<p>The fixture detail window name is composed of the manufacturer <em>(1)</em> + fixture name <em>(2)</em></p>

	<h3>3 - Wheels</h3>

		<p>If the profile has the Color/Gobo/Animation wheel available, these last will be displayed</p>

		<p>On mouse over, the picture will be zoomed:
		<br />
		<img src="ug/img/fixture-detail-gobo-zoom.png" alt="Zoom on a Gobo" /></p>

		<p><strong>Note: The open slot is never shown</strong></p>

	<h3>4 &amp; 6 - DMX Charts</h3>

		<p>The DMX Charts <em>(4)</em> are displayed by mode.</p>

		<p>Each mode name can be clicked to launch a search with the exact same DMX Charts:
		<br />
		<img src="ug/img/search-fixture-with-same-dmx-chart.png" alt="Search fixture profile with same DMX Charts" /></p>

		<p>Each mode display the channel type and their associated number:
		<br />
		<img src="ug/img/fixture-detail-dmx-chart-simplified.png" alt="DMX Chart" /></p>

		<p>You can switch the simplified view to the extended one to see the channel presets. Click on the "Show Fixture Preset" <em>(4)</em> button to switch:
		<br />
		<img src="ug/img/show-fixture-preset.png" alt="Show Fixture Preset" /></p>

		<p>As result, the channel presets are displayed:
		<br />
		<img src="ug/img/fixture-detail-dmx-chart-extended.png" alt="DMX Chart with presets" /></p>

	<h3>5 - Changelog</h3>

		<p>Inside the fixture changelog you can find the evolution bring on the profile between all release</p>

		<p>Click on the "Changelog" icon <em>(5)</em> to display it:
		<br />
		<img src="ug/img/fixture-detail-changelog.png" alt="Fixture changelog" /></p>

		<p><strong>Note: only the channel type, wheels and mode <em>(since 2014)</em> are tracked in the changelog</strong></p>

	<h3>7 - Close</h3>

		<p>This button is only displayed if the fixture detail page is display on a search page.</p>

		<p>Click on the "Close" button <em>(7)</em> to close the fixture detail window and get back to the search result.</p>

</div>
